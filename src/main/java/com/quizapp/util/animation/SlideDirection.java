package com.quizapp.util.animation;

/**
 * Defines direction constants for slide animations
 */
public enum SlideDirection {
    FROM_LEFT,    // Slide in from left side
    FROM_RIGHT,   // Slide in from right side
    FROM_TOP,     // Slide in from top
    FROM_BOTTOM,  // Slide in from bottom
    TO_LEFT,      // Slide out to left side
    TO_RIGHT,     // Slide out to right side
    TO_TOP,       // Slide out to top
    TO_BOTTOM     // Slide out to bottom
} 